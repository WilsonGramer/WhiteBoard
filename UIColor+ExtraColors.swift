import Foundation
import UIKit

// Extra colors for themes.
public extension UIColor {
    
    //MARK: Peach theme colors
    var peachLight: UIColor {
        return UIColor(red: 248/255, green: 200/255, blue: 165/255, alpha: 1)
    }
    
    var peachDark: UIColor {
        return UIColor(red: 241/255, green: 123/255, blue: 105/255, alpha: 1)
        //241	123	105
    }
    
    var navyBlue: UIColor {
        return UIColor(red: 4/255, green: 27/255, blue: 56/255, alpha: 1)
        //4	27	56
    }
    
    var lightBlue: UIColor {
        return UIColor(red: 182/255, green: 219/255, blue: 247/255, alpha: 1)
        //182	219	247
    }
}
